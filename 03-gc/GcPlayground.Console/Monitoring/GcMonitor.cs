using System;
using System.Threading;
using System.Threading.Tasks;

namespace GcPlayground.ConsoleApp.Monitoring
{
    /// <summary>
    /// Встроенный монитор GC для отслеживания метрик сборщика мусора в реальном времени.
    /// 
    /// Этот класс предоставляет простой способ мониторить GC без использования внешних инструментов.
    /// 
    /// Альтернативы:
    /// - dotnet-counters - официальный инструмент Microsoft для мониторинга
    /// - dotnet-trace - для трассировки событий GC
    /// - dotnet-dump - для анализа дампов памяти
    /// - PerfView - для детального анализа производительности
    /// 
    /// Использование этого класса:
    /// 1. Вызовите GcMonitor.StartMonitoring() для начала мониторинга
    /// 2. Монитор будет выводить метрики каждую секунду в отдельном потоке
    /// 3. Вызовите GcMonitor.StopMonitoring() для остановки
    /// </summary>
    public class GcMonitor
    {
        /// <summary>
        /// Флаг, указывающий активен ли мониторинг.
        /// Используется для управления фоновым потоком мониторинга.
        /// </summary>
        private static bool _isMonitoring = false;

        /// <summary>
        /// Запускает фоновый мониторинг GC.
        /// 
        /// Что происходит:
        /// 1. Проверяем, не запущен ли уже мониторинг
        /// 2. Устанавливаем флаг _isMonitoring = true
        /// 3. Запускаем фоновый поток (Task.Run)
        /// 4. Фоновый поток каждую секунду выводит метрики GC
        /// 
        /// Выводимые метрики:
        /// - Gen0, Gen1, Gen2 - количество сборок для каждого поколения
        /// - Memory - общий объем памяти в MB
        /// 
        /// Примечание:
        /// - Мониторинг работает в отдельном потоке, не блокируя основной поток
        /// - Можно запустить несколько раз, но мониторинг будет только один
        /// </summary>
        public static void StartMonitoring()
        {
            // Если мониторинг уже запущен, выходим
            if (_isMonitoring)
                return;

            // Устанавливаем флаг, что мониторинг активен
            _isMonitoring = true;

            // Запускаем фоновый поток для мониторинга
            Task.Run(() =>
            {
                // Цикл продолжается, пока _isMonitoring = true
                while (_isMonitoring)
                {
                    // Получаем текущий объем памяти
                    // false означает, что не вызываем сборку мусора перед измерением
                    var totalMemory = GC.GetTotalMemory(false);
                    
                    // Выводим метрики GC
                    // GC.CollectionCount(generation) - количество сборок для поколения
                    Console.WriteLine($"[MONITOR] Gen0: {GC.CollectionCount(0)}, " +
                        $"Gen1: {GC.CollectionCount(1)}, " +
                        $"Gen2: {GC.CollectionCount(2)}, " +
                        $"Memory: {totalMemory / 1024 / 1024}MB");
                    
                    // Ждем 1 секунду перед следующим выводом
                    Thread.Sleep(1000);
                }
            });
        }

        /// <summary>
        /// Останавливает фоновый мониторинг GC.
        /// 
        /// Что происходит:
        /// 1. Устанавливаем флаг _isMonitoring = false
        /// 2. Фоновый поток выходит из цикла while при следующей итерации
        /// 3. Мониторинг прекращается
        /// 
        /// Примечание:
        /// - Остановка происходит не мгновенно (максимум 1 секунда задержки)
        /// - После остановки можно снова вызвать StartMonitoring()
        /// </summary>
        public static void StopMonitoring()
        {
            // Устанавливаем флаг, что мониторинг должен остановиться
            _isMonitoring = false;
        }
    }
}
